

// (1)--> Arithmetic Operators:

        // Purpose: Perform basic mathematical operations.
        // Examples:
        //  + (addition): 5 + 3 results in 8
        //  - (subtraction): 5 - 3 results in 2
        //  * (multiplication): 5 * 3 results in 15
        //  / (division): 6 / 3 results in 2
        //  % (modulus): 5 % 3 results in 2 (remainder of division)
        //  ** (exponentiation): 2 ** 3 results in 8


// (2)-->  Assignment Operators:

        // Purpose: Assign values to variables.
        // Examples:
        //  = (simple assignment): let x = 5
        //  += (addition assignment): x += 3 (equivalent to x = x + 3)
        //  -= (subtraction assignment): x -= 2 (equivalent to x = x - 2)
        //  *= (multiplication assignment): x *= 4 (equivalent to x = x * 4)
        //  /= (division assignment): x /= 2 (equivalent to x = x / 2)


// (3)-->  Comparison Operators:

        // Purpose: Compare two values and return a boolean result.
        // Examples:
        //  == (equality): 5 == '5' is true (loose comparison)
        //  === (strict equality): 5 === '5' is false (strict comparison)(data type also check)
        //  != (inequality): 5 != 3 is true
        //  !== (strict inequality): 5 !== '5' is true
        //  > (greater than): 5 > 3 is true
        //  < (less than): 5 < 8 is true
        //  >= (greater than or equal to): 5 >= 5 is true
        //  <= (less than or equal to): 5 <= 7 is true



// (4)-->  String Operators:

        // Purpose: Work with strings.
        // Examples:
        // + (concatenation): 'Hello ' + 'World!' results in 'Hello World!'
        // += (concatenation assignment): let str = 'Hello'; str += ' World!'; results in 'Hello World!'


// (5)-->   Logical Operators:

        // Purpose: Combine boolean values and return a boolean result.
        // Examples:
        // && (logical AND): true && false is false
        // || (logical OR): true || false is true
        // ! (logical NOT): !true is false


// (6)-->    Bitwise Operators:

        // Purpose: Perform operations on the binary representations of numbers.
        // Examples:
        // & (bitwise AND): 5 & 3 results in 1 (binary 0101 & 0011)
        // | (bitwise OR): 5 | 3 results in 7 (binary 0101 | 0011)
        // ^ (bitwise XOR): 5 ^ 3 results in 6 (binary 0101 ^ 0011)
        // ~ (bitwise NOT): ~5 results in -6 (binary complement)
        // << (left shift): 5 << 1 results in 10 (binary 0101 shifted left)
        // >> (right shift): 5 >> 1 results in 2 (binary 0101 shifted right)
        // >>> (unsigned right shift): -5 >>> 1 results in 2147483642


// (7)-->     Ternary Operators:

        // Purpose: Shortened form of the if-else statement.
        // Syntax: condition ? expr1 : expr2
        // Examples:
        // let result = (5 > 3) ? 'Yes' : 'No' results in 'Yes'


// (8)-->      Type Operators:

        // Purpose: Determine or convert types.
        // Examples:
        // typeof (type checking): typeof 'Hello' results in 'string'
        // instanceof (instance checking): [] instanceof Array is true
        // Array.isArray() (check if an object is an array): Array.isArray([]) is true
